package com.syntax.class01;

public class DataTypesAndVariable {

	public static void main(String[] args) {
		/*byte - data type
		 * variable - variable/box/container name
		 * = assign
		 * 10 - value
		 */
		
		//storing whole numbers in variables
		byte variable=127;
		short variable1=32767;
		int var3=100000;  // most popular datatype to represent whole numbers
		long var4=10000000;
		
		//storing numbers with decimals
		float myFloat =10.99f;
		double myDouble=100.99; //most useful
		
		//storing single character
		char singleLetter='J';
		char specialCharacter='%';
		
		//store boolean values: yes or no
		boolean myBoo=true;
		boolean myBoo2=false;
		
		System.out.println(variable);
		System.out.println("variable");
		
		System.out.println(myBoo2);
		

	}

}
